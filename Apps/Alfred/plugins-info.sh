# Modifications
Here is a list of all plugins used

**Workflows**
- About Mac Details: about
- Search Apple Notes: [apple-notes][1]
- Base64 Encode Image: b64-image
- CD Terminal: cd
- Close All Notifications: close
- Convert Color Formats: colors
- Convert Various Units: [convert][2]
- Reference DevDocs: devdocs
- Reference DevHints: devhints
- Encode & Decode Strings: [encode][3]
- Generate a Markdown Table: [md-table][4]
- Search NPM Packages: [alfred-npms][5]
- Search Parcel Deliveries: [parcel][6]
- Create a Placeholder Img: [placeholder][7]
- Shorten URL: short-url
- Reference TailwindCSS Docs: [tailwindcss][8]
- Work With Things3 Todos: [things][9]
- Generate Unique UUID: uuid
- Open Folders in VSCode: vscode
- Get Weather Data: weather

**Snippets**
- Apple Keyboard Symbols: mac-symbols

**Terminal**
Script to open iTerm2 by default (vs terminal): iterm2

