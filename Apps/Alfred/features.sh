# New Search (Open Alfred)
⌘cmd + ␣space

# Search Files
open <filename>   # Open the file
find <filename>   # Reveal in finder

# Preview Item (Quick Look)
⇧shift    # or: ⌘cmd + y

# Clipboard History
⌥alt + ⌘cmd + c     # copy: ↵return

# Snippets
⌥alt + ⌘cmd + x     # Open snippet viewer
snip <snippet-name> # Search for a snippet

# Calculator (just type math)
1 * 3
(2+3) * (1+2)

# Dictionary
define <word>       # Open in dictionary app
spell <word>        # Copy a word

# Mini Music Player
itunes                  # Open the player
play / pause            # Play & Pause
next / back / previous  # Next, Back, Previous
random                  # Play random album
volmid                  # Set volume to 50%
mute                    # Mute the volume

# System
shutdown                # Shutdown the computer
restart                 # Restart the computer
sleep / sleepdisplays   # Sleep single or all displays
logout / lock           # Logout or Lock computer
screensaver             # Start screensaver
trash / emptytrash      # Open trash, Empty trash

# System Windows
hide / quit / quitall   # Hide, Quit single, Quit all
forcequit               # Force quit a window

# Sound
volup / voldown / mute  # Volume up, Volume down, Mute
