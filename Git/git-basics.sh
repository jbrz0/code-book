# Check git version
git version

# Show git remotes
git remote -v

# create new branch, and switch to it
git checkout -b <branch_name>

# Switch (only) to an existing branch
git checkout <branch_name>

# Setup git remotes
git remote add <remote_name> <remote_endpoint>
git remote add <different_name> <different_endpoint>
