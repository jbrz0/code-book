# "Easy to forget" query string & parameter formatting

# Query: https://en.wikipedia.org/wiki/Query_string
# Clean URLs: https://en.wikipedia.org/wiki/Clean_URL

# Single
www.yoursite.com/?myparam=123

# Multiple (chained)
www.yoursite.com?myparam1=123&myparam2=abc

# Formatting as a "Clean Url"
www.yoursite.com?category=12&pid=25   # unclean
www.yoursite.com/12/25                # clean
